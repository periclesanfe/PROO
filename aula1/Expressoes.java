public class Expressoes {
    public static void main(String[] args) {
        int numero1 = 10, numero2 = 20;

        System.out.println(numero1 + numero2);
        System.out.println(numero1 - numero2);
        System.out.println(numero1 * numero2);
        System.out.println(numero1 / numero2);
    }
}
